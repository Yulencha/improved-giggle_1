/*
Задача о коллекции функций: у вас есть массив функций, напишите код, который вызовет 
каждую функцию в этом массиве и выведет их порядковый номер. 
Однако, вызов каждой функции должен происходить только после вызова предыдущей функции.
Другими словами, нужно выполнить следующие шаги:
Вызвать первую функцию из массива.
После завершения работы первой функции вызвать вторую функцию.
После завершения работы второй функции вызвать третью функцию.
И так далее, пока все функции в массиве не будут вызваны по порядку.
*/

function executeFunctionsSequentially(functionsArray) {
  function executeNext(index) {
    // Проверяем, не вышли ли мы за пределы массива
    if (index < functionsArray.length) {
      // Вызываем функцию с текущим индексом
      console.log(`Вызов функции ${index + 1}`);
      functionsArray[index]();

      // Рекурсивно вызываем следующую функцию
      executeNext(index + 1);
    }
  }

  // Начинаем с первой функции
  executeNext(0);
}

// Тест 1: Массив с одной функцией
console.log("Тест 1: Массив с одной функцией");
executeFunctionsSequentially([() => console.log("Функция 1 выполнена")]);

// Тест 2: Массив с несколькими функциями
console.log("Тест 2: Массив с несколькими функциями");
executeFunctionsSequentially([
  () => console.log("Функция 1 выполнена"),
  () => console.log("Функция 2 выполнена"),
  () => console.log("Функция 3 выполнена"),
]);
